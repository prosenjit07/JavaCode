package student2;
public class Student2 {

     private String model;
     private int code;
     private String brand;
     private double price;

     //Setter methods
    public void setModel(String model) {
        this.model = model;
    }
     // Getter methods 
    public String getModel() {
        return this.model;
    }
    
    public void setBrand(String brand) {
        this.brand = brand;
    }
     // Getter methods 
    public String getBrand() {
        return this.brand;
    }
    
//Setter methods
    public void setCode(int a) {
        this.code = a;
    }
 // Getter methods 
    public int getCode() {
        return this.code;
    }

    public void setprice(double c) {
        this.price = c;
    }

    public double getprice() {
        return this.price;
    }
}
