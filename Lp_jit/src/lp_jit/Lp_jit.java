package lp_jit;
public class Lp_jit {

     private String name;
     private int id;
     private double cgpa;

     //Setter methods
    public void setName(String name) {
        this.name = name;
    }
     // Getter methods 
    public String getName() {
        return this.name;
    }
//Setter methods
    public void setId(int a) {
        this.id = a;
    }
 // Getter methods 
    public int getId() {
        return this.id;
    }

    public void setCgpa(double c) {
        this.cgpa = c;
    }

    public double getCgpa() {
        return this.cgpa;
    }
}
